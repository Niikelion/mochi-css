import { printSync } from "@swc/core"
import type { Expression, ObjectExpression, TemplateLiteral } from "@swc/core"
import type { ParsedFile, CreateTokenBinding } from "../extract.ts"

const HEADER = "// @generated — do not edit; regenerated from mochi-vanilla-react by codegen/index.ts\n"
const DUMMY_SPAN = { start: 0, end: 0, ctxt: 0 }
const TS_IGNORE = `// eslint-disable-next-line @typescript-eslint/ban-ts-comment\n// @ts-ignore — styled-system/ is generated by \`panda codegen\` before the build`
const INDENT = "    "

const PANDA_TOKEN_SCALES = new Set(["colors", "fontSizes", "radii", "space"])
const VARIANT_KEYS = new Set(["variants", "defaultVariants", "compoundVariants"])

// CSS scale → panda-generated CSS var prefix (for custom property values)
const SCALE_TO_VAR: Record<string, string> = {
    colors: "colors",
    space: "spacing",
    fontSizes: "font-sizes",
    radii: "radii",
    fonts: "fonts",
}

// Pseudo-selector → panda condition
const PSEUDO_MAP: Record<string, string> = {
    "&:hover": "_hover",
    "&:focus": "_focus",
    "&:active": "_active",
    "&:disabled": "_disabled",
    "&:checked": "_checked",
    "&::placeholder": "_placeholder",
    "&:focus-visible": "_focusVisible",
    "&:focus-within": "_focusWithin",
    "&:first-child": "_first",
    "&:last-child": "_last",
}

// Media query → panda responsive key
const MEDIA_MAP: Record<string, string> = {
    "@media (min-width: 640px)": "sm",
    "@media (min-width: 768px)": "md",
    "@media (min-width: 1024px)": "lg",
    "@media (min-width: 1280px)": "xl",
}

// Shorthand border → longhand parts
const BORDER_LONGHANDS: Record<string, { width: string; style: string; color: string }> = {
    border: { width: "borderWidth", style: "borderStyle", color: "borderColor" },
    borderTop: { width: "borderTopWidth", style: "borderTopStyle", color: "borderTopColor" },
    borderBottom: { width: "borderBottomWidth", style: "borderBottomStyle", color: "borderBottomColor" },
    borderLeft: { width: "borderLeftWidth", style: "borderLeftStyle", color: "borderLeftColor" },
    borderRight: { width: "borderRightWidth", style: "borderRightStyle", color: "borderRightColor" },
}

// 2-value shorthand → block/inline longhands
const BLOCK_INLINE: Record<string, { block: string; inline: string }> = {
    padding: { block: "paddingBlock", inline: "paddingInline" },
    margin: { block: "marginBlock", inline: "marginInline" },
    gap: { block: "rowGap", inline: "columnGap" },
}

function needsQuoting(key: string): boolean {
    return !/^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key)
}

function serializeKey(key: string): string {
    return needsQuoting(key) ? JSON.stringify(key) : key
}

function exprToSrc(expr: Expression): string {
    const r = printSync({
        type: "Module",
        body: [{ type: "ExpressionStatement", expression: expr, span: DUMMY_SPAN }],
        interpreter: null,
        span: DUMMY_SPAN,
    })
    return r.code.replace(/;\s*$/, "").trim()
}

// Resolve a token expression to a panda scale key (e.g. colors.text → "text")
function resolveTokenExpr(expr: Expression, known: Set<string>, bindings: CreateTokenBinding[]): string | null {
    if (expr.type === "Identifier") {
        if (expr.value === "font" && known.has("font")) return "mono"
        if (expr.value === "fadeIn") return "fadeIn"
    }

    if (expr.type === "MemberExpression" && expr.object.type === "Identifier") {
        const objName = expr.object.value
        const propNode = expr.property as unknown as { type: string; value?: string; expression?: Expression }

        if (propNode.type === "Identifier") {
            const propName = propNode.value!
            if (propName === "value") {
                const b = bindings.find(b => b.varName === objName)
                if (b) return `var(--${b.tokenName})`
            }
            if (PANDA_TOKEN_SCALES.has(objName) && known.has(objName)) return propName
        }

        if (propNode.type === "Computed") {
            const p = propNode.expression
            if (p && PANDA_TOKEN_SCALES.has(objName) && known.has(objName)) {
                if (p.type === "NumericLiteral") return String((p as { value: number }).value)
                if (p.type === "StringLiteral") return (p as { value: string }).value
            }
        }
    }

    return null
}

// Resolve to a CSS var reference (for use inside CSS custom property values)
function resolveTokenExprAsCssVar(expr: Expression, known: Set<string>): string | null {
    if (expr.type === "Identifier" && expr.value === "font" && known.has("font"))
        return "var(--fonts-mono)"

    if (expr.type === "MemberExpression" && expr.object.type === "Identifier") {
        const objName = expr.object.value
        const propNode = expr.property as unknown as { type: string; value?: string; expression?: Expression }
        const varPrefix = SCALE_TO_VAR[objName]
        if (!varPrefix || !known.has(objName)) return null

        if (propNode.type === "Identifier") {
            return `var(--${varPrefix}-${propNode.value})`
        }
        if (propNode.type === "Computed") {
            const p = propNode.expression
            if (p && p.type === "NumericLiteral") return `var(--${varPrefix}-${(p as { value: number }).value})`
            if (p && p.type === "StringLiteral") return `var(--${varPrefix}-${(p as { value: string }).value})`
        }
    }

    return null
}

function resolveTemplate(tmpl: TemplateLiteral, known: Set<string>, bindings: CreateTokenBinding[]): string | null {
    let result = ""
    for (let i = 0; i < tmpl.quasis.length; i++) {
        result += tmpl.quasis[i].cooked ?? tmpl.quasis[i].raw
        if (i < tmpl.expressions.length) {
            const s = resolveTokenExpr(tmpl.expressions[i] as Expression, known, bindings)
            if (s === null) return null
            result += s
        }
    }
    return result
}

function resolveComputedKey(expr: Expression, bindings: CreateTokenBinding[]): string | null {
    if (expr.type !== "MemberExpression" || expr.object.type !== "Identifier") return null
    const objName = expr.object.value
    const propNode = expr.property as unknown as { type: string; value?: string }
    if (propNode.type !== "Identifier" || propNode.value !== "variable") return null
    const b = bindings.find(b => b.varName === objName)
    return b ? `--${b.tokenName}` : null
}

function serializeExpr(expr: Expression, known: Set<string>, bindings: CreateTokenBinding[], depth: number): string {
    const token = resolveTokenExpr(expr, known, bindings)
    if (token !== null) return JSON.stringify(token)

    if (expr.type === "TemplateLiteral") {
        const resolved = resolveTemplate(expr as unknown as TemplateLiteral, known, bindings)
        if (resolved !== null) return JSON.stringify(resolved)
        return exprToSrc(expr)
    }

    if (expr.type === "StringLiteral") return JSON.stringify((expr as { value: string }).value)
    if (expr.type === "NumericLiteral") return String((expr as { value: number }).value)
    // Boolean → quoted string (for variant keys like "true"/"false")
    if (expr.type === "BooleanLiteral") return JSON.stringify(String((expr as { value: boolean }).value))
    if (expr.type === "ObjectExpression") return serializeObject(expr as ObjectExpression, known, bindings, depth)

    return exprToSrc(expr)
}

function serializeExprAsCssVar(expr: Expression, known: Set<string>, bindings: CreateTokenBinding[], depth: number): string {
    const cssVar = resolveTokenExprAsCssVar(expr, known)
    if (cssVar !== null) return JSON.stringify(cssVar)
    return serializeExpr(expr, known, bindings, depth)
}

function tryExpandBorder(
    key: string,
    tmpl: TemplateLiteral,
    known: Set<string>,
    bindings: CreateTokenBinding[],
): string[] | null {
    const parts = BORDER_LONGHANDS[key]
    if (!parts || tmpl.quasis.length !== 2 || tmpl.expressions.length !== 1) return null
    const prefix = (tmpl.quasis[0].cooked ?? tmpl.quasis[0].raw).trimEnd()
    const suffix = tmpl.quasis[1].cooked ?? tmpl.quasis[1].raw
    if (suffix !== "") return null
    const m = prefix.match(/^([\w.]+(?:px|em|rem|%)?)\s+(\w+)$/)
    if (!m) return null
    const colorToken = resolveTokenExpr(tmpl.expressions[0] as Expression, known, bindings)
    if (colorToken === null) return null
    return [
        `${parts.width}: ${JSON.stringify(m[1])}`,
        `${parts.style}: ${JSON.stringify(m[2])}`,
        `${parts.color}: ${JSON.stringify(colorToken)}`,
    ]
}

function tryExpandBlockInline(
    key: string,
    tmpl: TemplateLiteral,
    known: Set<string>,
    bindings: CreateTokenBinding[],
): string[] | null {
    const parts = BLOCK_INLINE[key]
    if (!parts || tmpl.quasis.length !== 3 || tmpl.expressions.length !== 2) return null
    const q0 = tmpl.quasis[0].cooked ?? tmpl.quasis[0].raw
    const q1 = tmpl.quasis[1].cooked ?? tmpl.quasis[1].raw
    const q2 = tmpl.quasis[2].cooked ?? tmpl.quasis[2].raw
    if (q0 !== "" || q1 !== " " || q2 !== "") return null
    const block = resolveTokenExpr(tmpl.expressions[0] as Expression, known, bindings)
    const inline = resolveTokenExpr(tmpl.expressions[1] as Expression, known, bindings)
    if (block === null || inline === null) return null
    return [
        `${parts.block}: ${JSON.stringify(block)}`,
        `${parts.inline}: ${JSON.stringify(inline)}`,
    ]
}

function serializeProps(
    properties: ObjectExpression["properties"],
    known: Set<string>,
    bindings: CreateTokenBinding[],
    depth: number,
): string[] {
    const pad = INDENT.repeat(depth)
    const lines: string[] = []

    for (const prop of properties) {
        if (prop.type !== "KeyValueProperty") continue

        const keyNode = prop.key as unknown as { type: string; value?: string; expression?: Expression }
        let rawKey: string | null = null
        let isComputedKey = false

        if (keyNode.type === "Identifier" || keyNode.type === "StringLiteral") {
            rawKey = keyNode.value ?? null
        } else if (keyNode.type === "Computed") {
            isComputedKey = true
            const resolved = resolveComputedKey(keyNode.expression as Expression, bindings)
            rawKey = resolved ?? exprToSrc(keyNode.expression as Expression)
        }

        if (rawKey === null) continue

        const valueExpr = prop.value as Expression

        if (!isComputedKey) {
            if (PSEUDO_MAP[rawKey]) {
                lines.push(`${pad}${PSEUDO_MAP[rawKey]}: ${serializeExpr(valueExpr, known, bindings, depth + 1)},`)
                continue
            }
            if (MEDIA_MAP[rawKey]) {
                lines.push(`${pad}${MEDIA_MAP[rawKey]}: ${serializeExpr(valueExpr, known, bindings, depth + 1)},`)
                continue
            }
            if (BORDER_LONGHANDS[rawKey] && valueExpr.type === "TemplateLiteral") {
                const expanded = tryExpandBorder(rawKey, valueExpr as unknown as TemplateLiteral, known, bindings)
                if (expanded) { lines.push(...expanded.map(l => `${pad}${l},`)); continue }
            }
            if (BLOCK_INLINE[rawKey] && valueExpr.type === "TemplateLiteral") {
                const expanded = tryExpandBlockInline(rawKey, valueExpr as unknown as TemplateLiteral, known, bindings)
                if (expanded) { lines.push(...expanded.map(l => `${pad}${l},`)); continue }
            }
        }

        // CSS custom property values need the var(--scale-key) format
        const keyStr = isComputedKey ? `[${JSON.stringify(rawKey)}]` : serializeKey(rawKey)
        const valueStr = !isComputedKey && rawKey.startsWith("--")
            ? serializeExprAsCssVar(valueExpr, known, bindings, depth + 1)
            : serializeExpr(valueExpr, known, bindings, depth + 1)
        lines.push(`${pad}${keyStr}: ${valueStr},`)
    }

    return lines
}

function serializeObject(node: ObjectExpression, known: Set<string>, bindings: CreateTokenBinding[], depth: number): string {
    if (node.properties.length === 0) return "{}"
    const closeIndent = INDENT.repeat(depth - 1)
    return `{\n${serializeProps(node.properties, known, bindings, depth).join("\n")}\n${closeIndent}}`
}

// Serialize a style config as panda cva({ base: {...}, variants: {...}, ... })
function serializeCvaConfig(config: ObjectExpression, known: Set<string>, bindings: CreateTokenBinding[]): string {
    const baseProps = config.properties.filter(p => {
        if (p.type !== "KeyValueProperty") return true
        return !VARIANT_KEYS.has((p.key as { value?: string }).value ?? "")
    })
    const specialProps = config.properties.filter(p => {
        if (p.type !== "KeyValueProperty") return false
        return VARIANT_KEYS.has((p.key as { value?: string }).value ?? "")
    })

    const baseNode: ObjectExpression = { ...config, properties: baseProps }
    const baseSrc = serializeObject(baseNode, known, bindings, 2)

    const lines = ["{", `    base: ${baseSrc},`]
    for (const prop of specialProps) {
        if (prop.type !== "KeyValueProperty") continue
        const key = (prop.key as { value?: string }).value ?? ""
        lines.push(`    ${key}: ${serializeObject(prop.value as ObjectExpression, known, bindings, 2)},`)
    }
    lines.push("}")
    return lines.join("\n")
}

interface VariantParam {
    name: string
    values: string[]
    isBoolean: boolean
    defaultValue: string | null
}

function extractVariantParams(config: ObjectExpression): VariantParam[] {
    const params: VariantParam[] = []
    const defaults: Record<string, string> = {}

    for (const prop of config.properties) {
        if (prop.type !== "KeyValueProperty") continue
        const key = (prop.key as { value?: string }).value ?? ""

        if (key === "variants") {
            const varObj = prop.value as ObjectExpression
            for (const vp of varObj.properties) {
                if (vp.type !== "KeyValueProperty") continue
                const vName = (vp.key as { value?: string }).value ?? ""
                if (!vName) continue
                const values = (vp.value as ObjectExpression).properties.flatMap(vvp => {
                    if (vvp.type !== "KeyValueProperty") return []
                    const k = (vvp.key as { value?: string }).value
                    return k ? [k] : []
                })
                params.push({
                    name: vName,
                    values,
                    isBoolean: values.length === 2 && values.includes("true") && values.includes("false"),
                    defaultValue: null,
                })
            }
        } else if (key === "defaultVariants") {
            for (const dp of (prop.value as ObjectExpression).properties) {
                if (dp.type !== "KeyValueProperty") continue
                const dKey = (dp.key as { value?: string }).value ?? ""
                const dVal = dp.value as Expression
                if (dVal.type === "StringLiteral") defaults[dKey] = (dVal as { value: string }).value
                else if (dVal.type === "BooleanLiteral") defaults[dKey] = String((dVal as { value: boolean }).value)
                else if (dVal.type === "NumericLiteral") defaults[dKey] = String((dVal as { value: number }).value)
            }
        }
    }

    for (const p of params) p.defaultValue = defaults[p.name] ?? null
    return params
}

function hasVariantsNode(config: ObjectExpression): boolean {
    return config.properties.some(p =>
        p.type === "KeyValueProperty" && (p.key as { value?: string }).value === "variants"
    )
}

function resolveDepth(filePath: string): string {
    const parts = filePath.split(/[/\\]/)
    const depth = parts.length - 1
    if (depth <= 0) return "."
    return Array(depth).fill("..").join("/")
}

export function generatePandaFile(parsed: ParsedFile, relPath: string): string {
    const { tokenImports, createTokenBindings: bindings, styledCalls, cssCalls } = parsed
    const known = new Set(tokenImports)
    const lines: string[] = [HEADER]

    const cssVarRefs = new Set(styledCalls.filter(c => c.cssVarRef).map(c => c.cssVarRef!))
    const needsCss = styledCalls.some(c => !c.cssVarRef && (!c.config || !hasVariantsNode(c.config)))
        || cssCalls.some(c => !cssVarRefs.has(c.varName))
    const needsCva = styledCalls.some(c => c.cssVarRef || (c.config && hasVariantsNode(c.config)))
    const hasHeadingPattern = styledCalls.some(c => c.cssVarRef)

    if (needsCss || needsCva) {
        lines.push(TS_IGNORE)
        const fns: string[] = []
        if (needsCss) fns.push("css")
        if (needsCva) fns.push("cva")
        lines.push(`import { ${fns.join(", ")} } from "styled-system/css"`)
    }

    if (hasHeadingPattern) {
        lines.push(`import { createElement, type HTMLProps } from "react"`)
    }
    lines.push(`import React from "react"`)
    lines.push("")

    // bindings are resolved to string literals during codegen — no runtime declarations needed

    const cssCallMap = new Map(cssCalls.map(c => [c.varName, c]))

    for (const cssCall of cssCalls) {
        if (cssVarRefs.has(cssCall.varName)) continue
        lines.push(`const _${cssCall.varName}Css = css(${serializeObject(cssCall.config, known, bindings, 2)})`)
    }

    for (const call of styledCalls) {
        const prefix = call.isExported ? "export " : ""
        const el = call.isComponent ? "div" : call.element

        if (call.cssVarRef) {
            // Heading pattern → cva() + createElement wrapper
            const config = cssCallMap.get(call.cssVarRef)?.config
            const cvaArg = config ? serializeCvaConfig(config, known, bindings) : "{ base: {} }"
            const variantParams = config ? extractVariantParams(config) : []

            lines.push(`const headingStyles = cva(${cvaArg})`)
            lines.push("")
            lines.push(`type HeadingProps = {`)
            lines.push(`    as?: "h1" | "h2" | "h3" | "h4"`)
            for (const v of variantParams) {
                lines.push(`    ${v.name}?: ${v.values.map(val => JSON.stringify(val)).join(" | ")}`)
            }
            lines.push(`} & HTMLProps<HTMLHeadingElement>`)
            lines.push("")

            const varNames = variantParams.map(v => v.name)
            const destructure = ["as: tag = \"h2\"", ...varNames, "className", "...props"].join(", ")
            const callArgs = varNames.length > 0 ? `{ ${varNames.join(", ")} }` : "{}"
            lines.push(`${prefix}const ${call.exportName} = ({ ${destructure} }: HeadingProps) => {`)
            lines.push(`    const cls = [headingStyles(${callArgs}), className].filter(Boolean).join(" ")`)
            lines.push(`    return createElement(tag, { className: cls, ...props })`)
            lines.push(`}`)
        } else if (call.config && hasVariantsNode(call.config)) {
            // Variant component → cva() + typed wrapper
            const recipeName = `_${call.exportName.charAt(0).toLowerCase() + call.exportName.slice(1)}Recipe`
            const variantParams = extractVariantParams(call.config)

            lines.push(`const ${recipeName} = cva(${serializeCvaConfig(call.config, known, bindings)})`)

            const variantTypeLines = variantParams.map(v =>
                v.isBoolean
                    ? `    ${v.name}?: boolean`
                    : `    ${v.name}?: ${v.values.map(val => JSON.stringify(val)).join(" | ")}`
            )
            const destructureParts = variantParams.map(v =>
                !v.isBoolean && v.defaultValue !== null ? `${v.name} = ${JSON.stringify(v.defaultValue)}` : v.name
            )
            const recipeArgs = variantParams.map(v =>
                v.isBoolean ? `${v.name}: ${v.name} ? "true" : "false"` : v.name
            )

            lines.push(`${prefix}const ${call.exportName} = ({`)
            lines.push(`    ${[...destructureParts, "className", "children", "...p"].join(", ")}`)
            lines.push(`}: {`)
            lines.push(...variantTypeLines.map(l => `${l},`))
            lines.push(`} & React.ComponentPropsWithoutRef<"${el}">) =>`)
            lines.push(`    <${el} {...p} className={[${recipeName}({ ${recipeArgs.join(", ")} }), className].filter(Boolean).join(" ")}>{children}</${el}>`)
        } else {
            // Non-variant → css() + simple wrapper
            const internalName = `_${call.exportName.charAt(0).toLowerCase() + call.exportName.slice(1)}Css`
            if (call.config) {
                lines.push(`const ${internalName} = css(${serializeObject(call.config, known, bindings, 2)})`)
            }
            const classSrc = call.config
                ? `[${internalName}, className].filter(Boolean).join(" ")`
                : `className ?? ""`
            lines.push(`${prefix}const ${call.exportName} = ({ className, children, ...p }: React.ComponentPropsWithoutRef<"${el}">) =>`)
            lines.push(`    <${el} {...p} className={${classSrc}}>{children}</${el}>`)
        }
    }

    return lines.join("\n") + "\n"
}

